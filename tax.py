# Basic Tax calculator

# Base amount before tax
amount = 200          
# Tax rate (7%)
tax = 0.07            
# Calculate total by adding tax to the amount
total = amount + amount * tax  
# Print the final amount with tax included
print(total)     



# Optional tax calulator using user input

# Ask the user for the amount and convert to a float (for decimals)
amount = float(input("Enter the amount: "))

# Tax rate (7%)
tax = 0.07   
# Calculate the total with tax                        
total = amount + amount * tax        
# Print the result
print(total)                      

